<template>
  <div class="register-container">
    <el-form ref="registerForm" :model="registerForm" :rules="registerRules" class="register-form" autocomplete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">用户注册</h3>
      </div>
      <div class="login-container">
        <router-link to="/login">已有账号</router-link>
      </div>
      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="registerForm.username"
          placeholder="用户名"
          name="username"
          type="text"
          tabindex="1"
          autocomplete="on"
        />
      </el-form-item>

      <el-tooltip v-model="capsTooltip" content="大写锁定已打开" placement="right" manual>
        <el-form-item prop="password">
          <span class="svg-container">
            <svg-icon icon-class="password" />
          </span>
          <el-input
            :key="passwordType"
            ref="password"
            v-model="registerForm.password"
            :type="passwordType"
            placeholder="密码"
            name="password"
            tabindex="2"
            autocomplete="on"
            @keyup.native="checkCapslock"
            @blur="capsTooltip = false"
          />
          <span class="show-pwd" @click="showPwd('passwordType')">
            <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
          </span>
        </el-form-item>
      </el-tooltip>
      <el-tooltip v-model="capsTooltip" content="大写锁定已打开" placement="right" manual>
        <el-form-item prop="checkPassword">
          <span class="svg-container">
            <svg-icon icon-class="password" />
          </span>
          <el-input
            :key="checkPasswordType"
            ref="checkPassword"
            v-model="registerForm.checkPassword"
            :type="checkPasswordType"
            placeholder="确认密码"
            name="checkPassword"
            tabindex="3"
            autocomplete="on"
            @keyup.native="checkCapslock"
            @blur="capsTooltip = false"
            @keyup.enter.native="handleRegister"
          />
          <span class="show-pwd" @click="showPwd('checkPasswordType')">
            <svg-icon :icon-class="checkPasswordType === 'password' ? 'eye' : 'eye-open'" />
          </span>
        </el-form-item>
      </el-tooltip>

      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleRegister">注册</el-button>
    </el-form>
  </div>
</template>

<script>
import { validUsername } from '@/utils/validate'
import { checkUsername, register } from '@/api/register'

export default {
  name: 'Register',
  data() {
    const validateUsername = async(rule, value, callback) => {
      if (!validUsername(value)) {
        callback(new Error('用户名必须以字母开头，且与字母、数字、"."、"_"、和"@"构成'))
      } else if (value) {
        await checkUsername(value)
          .then(response => {
            console.log(response)
            if (response.code !== 0) {
              callback(new Error('该用户名已被注册'))
            }
          })
      } else {
        callback()
      }
    }
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('密码不能少于6位数'))
      } else {
        callback()
      }
    }
    const validateCheckPassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('确认密码不能少于6位数'))
      } else if (value !== this.registerForm.password) {
        callback(new Error('两次输入密码不一致!'))
      } else {
        callback()
      }
    }
    return {
      registerForm: {
        username: '',
        password: '',
        checkPassword: ''
      },
      registerRules: {
        username: [
          { required: true, message: '用户名不能为空', trigger: 'blur' },
          { min: 4, max: 20, message: '长度在 4 到 20 个字符', trigger: 'blur' },
          { required: true, trigger: 'blur', validator: validateUsername }
        ],
        password: [
          { min: 6, max: 25, message: '长度在 6 到 25 个字符', trigger: 'blur' },
          { required: true, trigger: 'blur', validator: validatePassword }
        ],
        checkPassword: [{ required: true, trigger: 'blur', validator: validateCheckPassword }]
      },
      passwordType: 'password',
      checkPasswordType: 'password',
      capsTooltip: false,
      loading: false
    }
  },
  mounted() {
    if (this.registerForm.username === '') {
      this.$refs.username.focus()
    } else if (this.registerForm.password === '') {
      this.$refs.password.focus()
    }
  },
  methods: {
    checkCapslock({ shiftKey, key } = {}) {
      if (key && key.length === 1) {
        if (shiftKey && (key >= 'a' && key <= 'z') || !shiftKey && (key >= 'A' && key <= 'Z')) {
          this.capsTooltip = true
        } else {
          this.capsTooltip = false
        }
      }
      if (key === 'CapsLock' && this.capsTooltip === true) {
        this.capsTooltip = false
      }
    },
    showPwd(passwordType) {
      if (this[passwordType] === 'password') {
        this[passwordType] = ''
      } else {
        this[passwordType] = 'password'
      }
      this.$nextTick(() => {
        if (passwordType === 'passwordType') {
          this.$refs.password.focus()
        } else if (passwordType === 'checkPasswordType') {
          this.$refs.checkPassword.focus()
        }
      })
    },
    handleRegister() {
      this.$refs.registerForm.validate(valid => {
        if (valid) {
          this.loading = true
          register(this.registerForm)
            .then(response => {
              this.loading = false
              if (response.code === 0) {
                this.$message({
                  message: '注册成功！将在5s后跳转到登陆页',
                  type: 'success',
                  duration: 5 * 1000
                })
                setTimeout(() => {
                  this.$router.push({ path: '/login' })
                }, 5000)
              } else {
                this.$message({
                  message: response.message || 'Error',
                  type: 'warning',
                  duration: 5 * 1000
                })
              }
            })
            .catch(() => {
              this.loading = false
            })
        } else {
          console.log('提交失败!!')
          return false
        }
      })
    },
    getOtherQuery(query) {
      return Object.keys(query).reduce((acc, cur) => {
        if (cur !== 'redirect') {
          acc[cur] = query[cur]
        }
        return acc
      }, {})
    }
  }
}
</script>

